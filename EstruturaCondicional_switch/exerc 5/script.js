let numero = parseInt(prompt(`Digite um número para um mês! (de "1" até "12")`));
switch (numero) {
    case 1:
        alert(`Mês: Janeiro`);
        break;
    case 2:
        alert(`Mês: Fevereiro`);
        break;
    case 3:
        alert(`Mês: Março`);
        break;
    case 4:
        alert(`Mês: Abril`);
        break;
    case 5:
        alert(`Mês: Maio`);
        break;
    case 6:
        alert(`Mês: Junho`);
        break;
    case 7:
        alert(`Mês: Julho`);
        break;
    case 8:
        alert(`Mês: Agosto`);
        break;
    case 9:
        alert(`Mês: Setembro`);
        break;
    case 10:
        alert(`Mês: Outubro`);
        break;
    case 11:
        alert(`Mês: Novembro`);
        break;
    case 12:
        alert(`Mês: Dezembro`);
        break;
    default:
        alert("Mês inválido");
        break;
}